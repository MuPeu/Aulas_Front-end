//Variáveis
var banana //Antigo (evitar)
let chocolate //Escopo de bloco
const price = 10 //Constante (imutaável)
console.log(typeof banana)
console.log(typeof chocolate)
console.log(typeof price)

//TIPOS DE DADOS
//String --> Texto
let name = "Leandro"
let lastName = 'Corrêa'
console.log(typeof name)
console.log(typeof lastName)

//Number --> 10, 10.00
let age = 28
let height = 1.98
console.log(typeof age)
console.log(typeof height)

//Boolean --> true / false
let isApproved = true
let isAllowed = false
console.log(typeof isApproved)
console.log(typeof isAllowed)

//Undefined --> Indefinido
let sum
console.log(typeof sum)

//Null --> Nulo
let bag = null
console.log(typeof bag)

//NaN --> Not a Number
let result = 0 / "3DT"
console.log(typeof result)