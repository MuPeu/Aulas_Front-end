//OPERADORES MATEMÁTICOS
let a = 5, b = 2

console.log(a + b) //Adição
console.log(a - b) //Subtração
console.log(a * b) //Multiplicação
console.log(a / b) //Divisão
console.log(a % b) //Módulo (Resto de Divisão)
console.log(a ** b) //Exponenciação

//Incremento
console.log(`Antes do i++ ${a}`)
a++ //a = a+1
console.log(`Depois do i++ ${a}`)

//Decremento
console.log(`Antes do i-- ${b}`)
b-- //b = b-1
console.log(`Depois do i-- ${b}`)

let bag = 0
//Adiciona e Atribui
bag += 4 //bag = bag+4

//Subtrai e Atribui
bag -= 6 //bag = bag-6