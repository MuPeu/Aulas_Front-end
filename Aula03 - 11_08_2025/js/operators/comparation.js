//OPERADORES DE COMPARAÇÃO

console.log(5 == "5") //True (igualdade de valor)
//Comparação Estrita
console.log(5 === "5") //False (igualdade de valor e tipo)

console.log(5 != "5") //False (desigualdade de valor)
console.log(5 !== "5") //True (desigualdade de valor ou tipo)

console.log(7 > 5) //True
console.log(3 < 5) //True
console.log(5 >= 5) //True
console.log(4 <= 4) //True